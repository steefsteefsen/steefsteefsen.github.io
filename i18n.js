const LANG_NAMES = {
  de: 'Deutsch', en: 'English', it: 'Italiano', fr: 'Français',
  es: 'Español', pt: 'Português', da: 'Dansk', uk: 'Українська',
  ru: 'Русский', tr: 'Türkçe', he: 'עברית', ar: 'العربية',
  fa: 'فارسی', zh: '中文', ja: '日本語', ko: '한국어',
};

function applyLanguage(code) {
  const t = translations[code];
  if (!t) return;

  // Direction + lang attribute
  document.documentElement.lang = t.lang;
  document.documentElement.dir = t.dir;

  // Apply all data-i18n keys
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = t[key];
      } else if (el.tagName === 'BUTTON') {
        el.textContent = t[key];
      } else {
        el.textContent = t[key];
      }
    }
  });

  // AI notice banner
  const banner = document.getElementById('ai-translation-notice');
  if (banner) {
    banner.style.display = t.reviewed ? 'none' : 'block';
  }

  // Save preference
  localStorage.setItem('lang', code);

  // Update active state in switcher
  document.querySelectorAll('.lang-option').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === code);
  });
}

function initLanguageSwitcher() {
  const saved = localStorage.getItem('lang');
  const browser = navigator.language.split('-')[0];
  const initial = (saved && translations[saved]) ? saved
    : (translations[browser] ? browser : 'en');
  applyLanguage(initial);
}

document.addEventListener('DOMContentLoaded', () => {
  initLanguageSwitcher();
  document.querySelectorAll('.lang-option').forEach(btn => {
    btn.addEventListener('click', () => applyLanguage(btn.getAttribute('data-lang')));
  });
});
