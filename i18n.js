const LANG_NAMES = {
  de: 'Deutsch', en: 'English', it: 'Italiano', fr: 'Français',
  es: 'Español', pt: 'Português', da: 'Dansk', uk: 'Українська',
  ru: 'Русский', tr: 'Türkçe', he: 'עברית', ar: 'العربية',
  fa: 'فارسی', zh: '中文', ja: '日本語', ko: '한국어',
  ku: 'Kurdî',
};

function applyLanguage(code) {
  const t = translations[code];
  if (!t) return;

  // Direction + lang attribute
  document.documentElement.lang = t.lang;
  document.documentElement.dir = t.dir;

  // Apply all data-i18n keys
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = t[key];
      } else if (el.tagName === 'BUTTON') {
        el.textContent = t[key];
      } else {
        el.textContent = t[key];
      }
    }
  });

  // Apply all data-i18n-html keys (allows HTML content like links)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (t[key] !== undefined) {
      el.innerHTML = t[key];
    }
  });

  // AI notice banner
  const banner = document.getElementById('ai-translation-notice');
  if (banner) {
    banner.style.display = t.reviewed ? 'none' : 'block';
  }

  // Save preference
  localStorage.setItem('lang', code);

  // Update active state in switcher
  document.querySelectorAll('.lang-option').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === code);
  });
}

function initLanguageSwitcher() {
  const urlParam = new URLSearchParams(window.location.search).get('lang');
  const saved = localStorage.getItem('lang');
  const browser = navigator.language.split('-')[0];
  const initial = (urlParam && translations[urlParam]) ? urlParam
    : (saved && translations[saved]) ? saved
    : (translations[browser] ? browser : 'en');
  applyLanguage(initial);
}

if (typeof document !== 'undefined') {
  document.addEventListener('DOMContentLoaded', () => {
    initLanguageSwitcher();
    document.querySelectorAll('.lang-option').forEach(btn => {
      btn.addEventListener('click', () => {
        const lang = btn.getAttribute('data-lang');
        const url = new URL(window.location);
        url.searchParams.set('lang', lang);
        window.history.replaceState({}, '', url);
        applyLanguage(lang);
      });
    });
  });
}

// Node.js / Jest export
if (typeof module !== 'undefined' && module.exports) {
  module.exports = { translations, LANG_NAMES, applyLanguage, initLanguageSwitcher };
}
